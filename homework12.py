import math

def calculate_hypotenuse(a, b):
    """
    Функция для расчета длины гипотенузы прямоугольного треугольника по теореме Пифагора.

    :param a: Длина первого катета.
    :param b: Длина второго катета.
    :return: Длина гипотенузы.
    """
    return math.sqrt(a**2 + b**2)

def main():
    # Запрос длин сторон у пользователя
    a = float(input("Введите длину первого катета: "))
    b = float(input("Введите длину второго катета: "))

    # Расчет длины гипотенузы
    c = calculate_hypotenuse(a, b)

    # Вывод результата на экран
    print("Длина гипотенузы:", c)

    